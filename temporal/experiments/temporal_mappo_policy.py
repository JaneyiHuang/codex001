from __future__ import annotations

import os
from typing import Any, Dict, List

import numpy as np
import torch

from temporal.env import MECEnv
from temporal.models import TemporalActor


class TemporalMAPPOPolicy:
    def __init__(
        self,
        cfg,
        model_path: str,
        device: str = "cpu",
        policy_name: str = "temporal_distilled_mappo",
        max_repeat: int | None = None,
        repeat_scale: float | None = None,
        safety_enabled: bool = True,
        safe_energy_min: float = 0.12,
        safe_queue_threshold: float = 0.80,
        safe_obs_change_threshold: float = 0.35,
        safe_channel_drop_threshold: float = 0.35,
    ):
        self.name = policy_name
        if not os.path.exists(model_path):
            raise FileNotFoundError(
                f"Cannot find temporal MAPPO actor: {model_path}\n"
                "Please run temporal/temporal_distill_mappo_actor.py first."
            )

        ckpt = torch.load(model_path, map_location=device)
        if not ckpt.get("temporal_actor", False):
            raise ValueError(
                "Temporal policy expects a checkpoint generated by "
                "temporal_distill_mappo_actor.py."
            )

        hidden_dims = ckpt.get("actor_hidden_dims")
        if hidden_dims is None:
            raise ValueError("Temporal checkpoint must contain actor_hidden_dims.")

        self.device = device
        self.actor = TemporalActor(
            obs_dim=cfg.obs_dim,
            n_actions=cfg.n_actions,
            hidden_dims=hidden_dims,
        ).to(device)
        self.actor.load_state_dict(ckpt["actor"])
        self.actor.eval()

        self.repeat_scale = float(repeat_scale if repeat_scale is not None else ckpt.get("repeat_scale", 5.0))
        self.max_repeat = int(max_repeat if max_repeat is not None else ckpt.get("max_repeat", 5))
        self.safety_enabled = bool(safety_enabled)
        self.safe_energy_min = float(safe_energy_min)
        self.safe_queue_threshold = float(safe_queue_threshold)
        self.safe_obs_change_threshold = float(safe_obs_change_threshold)
        self.safe_channel_drop_threshold = float(safe_channel_drop_threshold)

        self.n_agents = cfg.M
        self.reset_episode()

        params = ckpt.get("pruned_params", "unknown")
        print(f"Loaded {self.name} actor from: {model_path}")
        print(
            f"Temporal hidden dims: {hidden_dims}, params: {params}, "
            f"repeat_scale={self.repeat_scale}, max_repeat={self.max_repeat}, "
            f"safety={self.safety_enabled}"
        )

    def reset_episode(self) -> None:
        self.cached_actions = np.zeros(self.n_agents, dtype=np.int64)
        self.remaining_repeats = np.zeros(self.n_agents, dtype=np.int64)
        self.last_decision_obs = np.zeros((self.n_agents, 0), dtype=np.float32)
        self.has_decision_obs = np.zeros(self.n_agents, dtype=bool)
        self.last_env_t = None

        self.decision_count = 0
        self.inference_call_count = 0
        self.reused_action_count = 0
        self.safety_interrupt_count = 0
        self.predicted_repeat_sum = 0.0
        self.predicted_repeat_count = 0

    def _ensure_episode_state(self, env: MECEnv, obs: np.ndarray) -> None:
        if self.last_env_t is not None and env.t == 0 and self.last_env_t != 0:
            self.reset_episode()
        if self.last_decision_obs.shape != obs.shape:
            self.last_decision_obs = np.zeros_like(obs, dtype=np.float32)

    def _safety_interrupt(self, obs: np.ndarray, m: int) -> bool:
        if not self.safety_enabled or not self.has_decision_obs[m]:
            return False

        obs_m = obs[m]
        prev_obs_m = self.last_decision_obs[m]

        energy_low = obs_m[1] <= self.safe_energy_min
        queue_pressure = max(obs_m[4], obs_m[5], obs_m[8]) >= self.safe_queue_threshold

        critical_indices = np.array([1, 4, 5, 6, 7, 8, 9], dtype=np.int64)
        critical_change = float(np.max(np.abs(obs_m[critical_indices] - prev_obs_m[critical_indices])))
        obs_changed = critical_change >= self.safe_obs_change_threshold

        channel_drop = False
        if self.cached_actions[m] == 1:
            channel_drop = (prev_obs_m[3] - obs_m[3]) >= self.safe_channel_drop_threshold

        return bool(energy_low or queue_pressure or obs_changed or channel_drop)

    def select_actions(self, env: MECEnv, obs: np.ndarray) -> np.ndarray:
        self._ensure_episode_state(env, obs)

        actions = np.zeros(env.M, dtype=np.int64)
        decision_agents: List[int] = []

        for m in range(env.M):
            can_reuse = self.remaining_repeats[m] > 0
            interrupted = can_reuse and self._safety_interrupt(obs, m)
            if can_reuse and not interrupted:
                actions[m] = self.cached_actions[m]
                self.remaining_repeats[m] -= 1
                self.reused_action_count += 1
            else:
                if interrupted:
                    self.safety_interrupt_count += 1
                    self.remaining_repeats[m] = 0
                decision_agents.append(m)

        if decision_agents:
            batch_obs = torch.tensor(obs[decision_agents], dtype=torch.float32, device=self.device)
            with torch.no_grad():
                pred_actions, pred_repeats = self.actor.predict_temporal(
                    batch_obs,
                    repeat_scale=self.repeat_scale,
                    max_repeat=self.max_repeat,
                )
            pred_actions_np = pred_actions.cpu().numpy().astype(np.int64)
            pred_repeats_np = pred_repeats.cpu().numpy().astype(np.int64)
            self.inference_call_count += 1

            for local_idx, m in enumerate(decision_agents):
                action = int(pred_actions_np[local_idx])
                repeat = int(pred_repeats_np[local_idx])
                actions[m] = action
                self.cached_actions[m] = action
                self.remaining_repeats[m] = repeat
                self.last_decision_obs[m] = obs[m]
                self.has_decision_obs[m] = True
                self.decision_count += 1
                self.predicted_repeat_sum += repeat
                self.predicted_repeat_count += 1

        self.last_env_t = env.t
        return actions

    def get_episode_stats(self) -> Dict[str, Any]:
        pred_repeat_mean = (
            self.predicted_repeat_sum / self.predicted_repeat_count
            if self.predicted_repeat_count > 0
            else 0.0
        )
        return {
            "decision_count": float(self.decision_count),
            "inference_call_count": float(self.inference_call_count),
            "reused_action_count": float(self.reused_action_count),
            "safety_interrupt_count": float(self.safety_interrupt_count),
            "predicted_repeat_mean": float(pred_repeat_mean),
        }
